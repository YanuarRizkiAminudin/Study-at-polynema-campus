public class NasabahYanuar {
    String norekYanuar;
    String namaYanuar;
    String nikYanuar;
    int umurYanuar;
    double saldoYanuar;

    public NasabahYanuar(String noYanuar, String nmYanuar, String nikYanuar, int umurYanuar, double sdoYanuar) {
        this.norekYanuar = noYanuar;
        this.namaYanuar = nmYanuar;
        this.nikYanuar = nikYanuar;
        this.umurYanuar = umurYanuar;
        this.saldoYanuar = sdoYanuar;
    }
}
